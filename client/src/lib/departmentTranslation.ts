/**
 * Translates a department name to Chinese.
 * Returns the Chinese translation if a keyword match is found,
 * otherwise returns the original English name.
 */
export function translateDepartment(department: string | null | undefined): string {
  if (!department) return '';
  for (const [pattern, translation] of DEPT_KEYWORD_MAP) {
    if (pattern.test(department)) {
      return translation;
    }
  }
  return department; // fallback to English
}
